class AgentState(TypedDict):
    all_tc:       dict
    page_indexes: dict
    bm25_indexes: dict
    schema:       dict
    profiles:     dict   # NEW: {fiscal_year: profile_dict}
    facts:        dict
    validation:   dict
    failed_fields:list
    retry_count:  int
    top_k:        int
    log:          list

def node_extract(state:AgentState) -> AgentState:
    facts=dict(state.get('facts',{}))
    failed=state.get('failed_fields',[])
    log=list(state.get('log',[]))
    top_k=state.get('top_k',3)
    profiles=state.get('profiles',{})
    if failed:
        metrics_needed=[m for m in BLOCK_MAP if any(m in k for k in failed)]
        log.append(f'Re-extracting {len(metrics_needed)} failed fields (retry {state["retry_count"]})')
    else:
        metrics_needed=list(BLOCK_MAP.keys())
    block_groups=defaultdict(list)
    for m in metrics_needed: block_groups[BLOCK_MAP[m]].append(m)
    for fiscal_year,tc_data in state['all_tc'].items():
        tc=tc_data['tc']
        idx=state['page_indexes'].get(fiscal_year,{})
        bm25,bp=state['bm25_indexes'].get(fiscal_year,(None,None))
        profile=profiles.get(fiscal_year,{}) # Get the profile for this fiscal_year
        # Apply the profile to the canonical map before extraction
        apply_profile(profile)

        for block,metrics in block_groups.items():
            data=retrieve(block,ENTITY,idx,tc,bm25,bp,metrics,top_k)
            if not data: log.append(f'MISS: {block} ({fiscal_year})'); continue
            pa=agent_prompt('A',metrics,data['text'],data['pages'],profile)
            pb=agent_prompt('B',metrics,data['text'],data['pages'],profile)
            ra=safe_call(pa); rb=safe_call(pb)
            result=do_consensus(ra,rb)
            for k in list(result): result[k]=do_verify(result[k],tc,block_pages=data['pages'])
            for k,v in result.items():
                if isinstance(v,dict) and not v.get('year'): v['year']=fiscal_year
            facts.update(result)
            log.append(f'Extracted {block} ({fiscal_year}): src={data["src"]} pages={data["pages"]}')
    facts=do_derive(facts)
    return {**state,'facts':facts,'log':log,'failed_fields':[]}

def node_validate(state:AgentState) -> AgentState:
    facts=state['facts']; log=list(state['log']); validation={}; failed=[]
    years=set(f.get('year') for f in facts.values() if isinstance(f,dict) and f.get('year'))
    for yr in years:
        ta=facts.get(f'total_assets__{yr}',{}).get('value')
        te=facts.get(f'total_equity__{yr}',{}).get('value')
        tl=facts.get(f'total_liabilities__{yr}',{}).get('value')
        if ta and te and tl:
            delta=abs(ta-te-tl)/ta; ok=delta<0.02
            validation[f'balance_sheet_{yr}']='PASS' if ok else f'FAIL (delta={delta:.1%})'
            if not ok:
                failed.extend([f'total_assets__{yr}',f'total_equity__{yr}',f'total_liabilities__{yr}'])
                log.append(f'Validation FAIL {yr}: delta={delta:.1%}')
            else: log.append(f'Validation PASS {yr}')
    return {**state,'validation':validation,'failed_fields':failed,'log':log}

def node_reflect(state:AgentState) -> AgentState:
    log=list(state['log']); new_top_k=min(state['top_k']+2,6)
    log.append(f'REFLECT: expand top_k {state["top_k"]}->{new_top_k}, retry {state["retry_count"]+1}/3')
    return {**state,'top_k':new_top_k,'retry_count':state['retry_count']+1,'log':log}

def route_after_validate(state:AgentState) -> str:
    return 'reflect' if state['failed_fields'] and state['retry_count']<3 else 'output'

def node_output(state:AgentState) -> AgentState:
    log=list(state['log']); log.append('Pipeline complete. Ready for Excel output.')
    return {**state,'log':log}

def build_graph():
    g=StateGraph(AgentState)
    g.add_node('extract',node_extract)
    g.add_node('validate',node_validate)
    g.add_node('reflect',node_reflect)
    g.add_node('output',node_output)
    g.set_entry_point('extract')
    g.add_edge('extract','validate')
    g.add_conditional_edges('validate',route_after_validate,{'reflect':'reflect','output':'output'})
    g.add_edge('reflect','extract')
    g.add_edge('output',END)
    return g.compile()

