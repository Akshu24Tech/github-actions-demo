def _extract_page_text(pg) -> str:
    text = pg.extract_text(layout=True) or ''
    table_text = ''
    try:
        for table in (pg.extract_tables() or []):
            rows = []
            for row in table:
                cleaned = [str(c).strip().replace('\n',' ') if c else '' for c in row]
                if any(cleaned): rows.append(' | '.join(cleaned))
            if rows: table_text += '\n[TABLE]\n' + '\n'.join(rows) + '\n[/TABLE]\n'
    except Exception: pass
    return text + table_text

def _is_table_continuation(text: str) -> bool:
    """
    Detect if a page starts with a table continuation (no clear statement header).
    Signs: starts with pipe-separated row or a number, no new section header.
    Used by stitch_multipage_tables to decide whether to join pages.
    """
    lines = [l.strip() for l in text.split('\n') if l.strip()]
    if not lines: return False
    first_content = lines[0]
    # Starts with a data row (pipe-delimited) or a number-heavy line
    if first_content.startswith('|') or first_content.count('|') >= 2:
        return True
    # No financial statement header on first 3 lines
    HEADERS = ['balance sheet', 'profit and loss', 'cash flow', 'statement of',
               'income statement', 'financial position']
    top = ' '.join(lines[:3]).lower()
    if any(h in top for h in HEADERS):
        return False
    # If page is mostly numbers + short labels = continuation
    alpha_ratio = sum(c.isalpha() for c in first_content) / max(len(first_content), 1)
    if alpha_ratio < 0.4 and any(c.isdigit() for c in first_content):
        return True
    return False

def stitch_multipage_tables(tc: dict, pages: list) -> tuple:
    """
    Given a list of pages for a block, stitch continuation pages together.
    Returns (stitched_text, page_range) where page_range is the full list
    of pages actually used — for accurate source attribution.

    Example: Balance Sheet starts on page 190, continues on 191.
    Both pages are stitched. Every fact extracted from this block
    gets pages=[190, 191] as its source, not just [190].

    Also checks the NEXT page after the block — it may contain
    continuation rows of a table that started on the last block page.
    """
    if not pages:
        return '', []

    pages = sorted(pages)
    used_pages = list(pages)

    # Check if page immediately after the block is a continuation
    last_page = pages[-1]
    next_page = last_page + 1
    next_text = tc.get(str(next_page), '')
    if next_text and _is_table_continuation(next_text):
        used_pages.append(next_page)
        # Check one more (some tables span 3 pages)
        next_next = next_page + 1
        nn_text = tc.get(str(next_next), '')
        if nn_text and _is_table_continuation(nn_text):
            used_pages.append(next_next)

    # Stitch all pages with a clear separator that includes page number
    parts = []
    for pg in used_pages:
        page_text = tc.get(str(pg), '')
        if page_text.strip():
            parts.append(f'[PAGE {pg}]\n{page_text}')

    stitched = '\n\n---\n\n'.join(parts)
    return stitched, used_pages

