import os

def apply_profile(profile: dict):
    """
    Applies a company-specific profile to the CANONICAL map.
    This merges company-specific synonyms on top of the BASE_CANONICAL.
    """
    global CANONICAL, _REV
    # Make a deep copy to ensure we don't modify the base canonical directly
    CANONICAL = {k: list(v) for k, v in BASE_CANONICAL.items()}

    # Merge profile-specific synonyms
    profile_synonyms = profile.get('synonyms', {})
    for metric, synonyms in profile_synonyms.items():
        if metric in CANONICAL:
            # Add new synonyms without duplicating existing ones
            CANONICAL[metric].extend([s for s in synonyms if s not in CANONICAL[metric]])
        else:
            CANONICAL[metric] = list(synonyms)

    # Rebuild _REV after modifying CANONICAL
    _REV = _rebuild_rev(CANONICAL)

def profile_pdf(pdf_hash: str, tc: dict) -> dict:
    """
    Detects the best matching company profile based on keyword density in the PDF.
    Loads config files from CONFIGS_DIR.
    """
    cache_path = f'{CACHE_DIR}{pdf_hash}_profile.json'
    if Path(cache_path).exists():
        print(f'  Profiler cache hit: {pdf_hash}')
        return json.loads(Path(cache_path).read_text())

    profiles = []
    for f in os.listdir(CONFIGS_DIR):
        if f.endswith('.json'):
            try:
                with open(os.path.join(CONFIGS_DIR, f), 'r') as config_file:
                    profiles.append(json.load(config_file))
            except Exception as e:
                print(f'  Error loading config {f}: {e}')

    if not profiles:
        print('  No company profiles found. Using generic_corporate.')
        # Fallback to a generic profile if no configs are found
        return {'profile_id': 'generic_corporate', 'company_type': 'Generic Corporate', 'unit': 'INR crore', 'detection_scores': {}}

    # Combine all text for scoring
    full_text = ' '.join(tc.values()).lower()

    best_profile = None
    highest_score = -1
    detection_scores = {}

    for profile in profiles:
        score = 0
        keywords = profile.get('keywords', [])
        for kw in keywords:
            if kw.lower() in full_text:
                score += 1
        detection_scores[profile.get('profile_id', 'unknown')] = score

        if score > highest_score:
            highest_score = score
            best_profile = profile

    if not best_profile: # Fallback to generic if no keywords match
        print('  No profile keywords matched. Using generic_corporate.')
        for profile in profiles:
            if profile.get('profile_id') == 'generic_corporate':
                best_profile = profile
                break
        if not best_profile: # If generic_corporate isn't explicitly defined, use the first one as a fallback
            best_profile = profiles[0]

    # Add detection scores to the chosen profile for logging/debugging
    best_profile['detection_scores'] = detection_scores

