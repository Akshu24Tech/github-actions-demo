def bm25_retrieve(bm25,pages,query_terms:list,top_k:int=3) -> list:
    scores=bm25.get_scores(query_terms)
    ranked=sorted(zip(pages,scores),key=lambda x:-x[1])
    return [int(p) for p,s in ranked[:top_k] if s>0]

def retrieve(block:str,entity:str,idx:dict,tc:dict,
             bm25=None,bm25_pages=None,metrics:list=None,top_k:int=3) -> dict|None:
    """
    Retrieve text for a block. Uses stitch_multipage_tables to:
    1. Join continuation pages (tables that span page boundaries)
    2. Return the exact full page range used — for precise source attribution.
    Every fact extracted from returned text gets 'pages' as its source.
    """
    # Fix 4: try both standalone AND consolidated — whichever exists
    # Handles BMW (consolidated only) and Infosys (standalone primary)
    alt = 'consolidated' if entity == 'standalone' else 'standalone'
    for key in [f'{block}_{entity}', f'{block}_unknown', block,
                f'{block}_{alt}',   # try opposite entity if primary misses
                f'{block}_consolidated', f'{block}_standalone']:
        if key in idx:
            raw_pages = sorted(idx[key]['pages'])
            # Stitch + detect continuation pages
            text, used_pages = stitch_multipage_tables(tc, raw_pages)
            if text.strip():
                if used_pages != raw_pages:
                    print(f'    Table continuation: {block} extended {raw_pages} → {used_pages}')
                return {'text': text, 'pages': used_pages, 'src': 'page_index'}
    if bm25 and metrics:
        terms=[]
        for m in metrics:
            for v in CANONICAL.get(m,[m]): terms.extend(v.lower().split())
        raw_pages=bm25_retrieve(bm25,bm25_pages,list(set(terms)),top_k)
        if raw_pages:
            text, used_pages = stitch_multipage_tables(tc, raw_pages)
            print(f'    BM25 fallback: {block} -> pages {used_pages}')
            return {'text':text,'pages':used_pages,'src':'bm25'}
    return None

