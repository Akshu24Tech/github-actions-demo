DERIVE={
    'total_liabilities':{'fn':lambda r:r['total_assets']-r['total_equity'],'deps':['total_assets','total_equity']},
    'ebitda':{'fn':lambda r:r['profit_before_tax']+r['finance_cost']+r['depreciation'],'deps':['profit_before_tax','finance_cost','depreciation']},
    'free_cash_flow':{'fn':lambda r:r['operating_cash_flow']-abs(r['capex']),'deps':['operating_cash_flow','capex']},
    'net_profit_margin':{'fn':lambda r:round(r['profit_after_tax']/r['revenue']*100,2),'deps':['profit_after_tax','revenue']},
    'current_ratio':{'fn':lambda r:round(r['total_current_assets']/r['total_current_liabilities'],2),'deps':['total_current_assets','total_current_liabilities']},
    'debt_to_equity':{'fn':lambda r:round(r['total_liabilities']/r['total_equity'],2),'deps':['total_liabilities','total_equity']},
}

def do_derive(facts):
    years=set(f.get('year') for f in facts.values() if isinstance(f,dict) and f.get('year'))
    for metric,rule in DERIVE.items():
        for yr in years:
            k=f'{metric}__{yr}'
            if k in facts and facts[k].get('value') is not None: continue
            deps={d:facts[f'{d}__{yr}']['value'] for d in rule['deps']
                 if f'{d}__{yr}' in facts and facts[f'{d}__{yr}'].get('value') is not None}
            if len(deps)==len(rule['deps']):
                try: facts[k]={'metric':metric,'year':yr,'value':rule['fn'](deps),'confidence':1.0,'flag':'DERIVED','unit':'INR crore'}
                except (ZeroDivisionError,TypeError): pass
    return facts

