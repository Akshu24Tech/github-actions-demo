TOC_PATTERN = r'(Balance Sheet|Statement of (?:Profit|Cash)|Profit (?:and|&) Loss|Cash Flow|Financial Highlights?)[\s\.]+([0-9]{1,3})'

def _auto_offset_enhanced(tc: dict) -> int | None:
    FOOTER_NUM    = re.compile(r'(\d+)\s*$')
    REPORT_FOOTER = re.compile(r'annual\s+report|integrated\s+report', re.IGNORECASE)
    LONE_INT      = re.compile(r'^\s*(\d{1,4})\s*$')

    votes = {}
    total = len(tc)
    step  = max(1, (total - 5) // 40)

    for i in range(5, total, step):
        text  = tc.get(str(i + 1), '')
        lines = [l.strip() for l in text.split('\n') if l.strip()]

        for line in lines[:3] + lines[-3:]:
            # Pattern 1: standalone number (original)
            m = LONE_INT.match(line)
            if m:
                n = int(m.group(1))
                if 5 < n < total * 3:
                    off = (i + 1) - n
                    votes[off] = votes.get(off, 0) + 1

            # Pattern 2: "Report YYYY  NNN" in footer (Infosys style)
            if REPORT_FOOTER.search(line):
                m = FOOTER_NUM.search(line)
                if m:
                    n = int(m.group(1))
                    if 5 < n < total * 3:
                        off = (i + 1) - n
                        votes[off] = votes.get(off, 0) + 2  # stronger signal

    if not votes:
        return None
    best = max(votes, key=votes.get)
    return best if votes[best] >= 3 else None

def build_page_index(h:str, tc:dict, pdf_path:str=None) -> dict:
    p=f'{CACHE_DIR}{h}_index.json'
    if Path(p).exists(): return json.loads(Path(p).read_text())
    idx={}

    offset = 0
    if pdf_path:
        offset = _auto_offset_enhanced(tc) or 0

    toc=''.join(t for pg,t in tc.items() if int(pg)<=12)
    for lbl,pgn in re.findall(
        TOC_PATTERN,
        toc,re.IGNORECASE):
        k=_bk(lbl)
        real_page = int(pgn) + offset   # <-- the fix
        if k and k not in idx: idx[k]={'pages':[real_page],'src':'toc'}
    HDRS={'balance_sheet':['Balance Sheet','Statement of Financial Position'],
          'income_statement':['Statement of Profit and Loss','Income Statement','Profit and Loss'],
          'cash_flow':['Statement of Cash Flows','Cash Flow Statement'],
          'fin_highlights':['Financial Highlights','Results of Operations']}
    for pg,text in tc.items():
        if not text.strip(): continue
        lines = text.split('\n')
        top    = '\n'.join(lines[:10]).lower()   # first 10 lines
        bottom = '\n'.join(lines[-3:]).lower()   # last 3 lines
        scan   = top + ' ' + bottom
        ctx    = text.lower()[:400]
        ent    = 'standalone' if 'standalone' in ctx else ('consolidated' if 'consolidated' in ctx else 'unknown')
        for blk,hdrs in HDRS.items():
            for h2 in hdrs:
                if fuzz.partial_ratio(h2.lower(), scan) > 82:
                    key=f'{blk}_{ent}'
                    if key not in idx: idx[key]={'pages':[int(pg)],'src':'header'}
                    elif int(pg) not in idx[key]['pages']: idx[key]['pages'].append(int(pg))
                    break
    KW={'balance_sheet':['total assets','total equity','non-current assets'],
        'income_statement':['revenue from operations','profit before tax','total expenses'],
        'cash_flow':['cash flows from operating','net cash from operating','cash generated from']}
    for pg,text in tc.items():
        tl=text.lower(); ctx=tl[:400]
        ent='standalone' if 'standalone' in ctx else('consolidated' if 'consolidated' in ctx else 'unknown')
        for blk,kws in KW.items():
            if sum(1 for kw in kws if kw in tl)>=2:
                key=f'{blk}_{ent}'
                if key not in idx: idx[key]={'pages':[int(pg)],'src':'keyword'}
                elif int(pg) not in idx[key]['pages']: idx[key]['pages'].append(int(pg))
    for k in idx: idx[k]['pages']=sorted(set(idx[k]['pages']))
    Path(p).write_text(json.dumps(idx,indent=2))
    return idx

def _bk(l):
    ll=l.lower()
    if 'balance' in ll or 'financial position' in ll: return 'balance_sheet'
    if 'profit' in ll or 'loss' in ll or 'income' in ll: return 'income_statement'
    if 'cash' in ll: return 'cash_flow'
    if 'highlight' in ll or 'results' in ll: return 'fin_highlights'
    return ''

