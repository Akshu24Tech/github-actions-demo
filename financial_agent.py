def schema_mapper(excel_path:str, tc:dict, pdf_hash:str, idx:dict={}) -> dict:
    import hashlib as hl
    tmpl_hash=hl.md5(Path(excel_path).read_bytes()).hexdigest()[:8]
    cache=f'{CACHE_DIR}{pdf_hash}_{tmpl_hash}_schema_v3.json'
    if Path(cache).exists():
        print('  Schema Mapper: cache hit'); return json.loads(Path(cache).read_text())
    wb=load_workbook(excel_path)
    headers=[]
    for sn in wb.sheetnames:
        ws=wb[sn]
        for row in ws.iter_rows():
            for cell in row:
                if cell.value and str(cell.value).strip(): headers.append(str(cell.value).strip())
    headers=list(set(headers))[:30]
    fin_pages=[]
    for pk in [f'income_statement_{ENTITY}',f'balance_sheet_{ENTITY}',f'cash_flow_{ENTITY}',
               'income_statement_standalone','balance_sheet_standalone','income_statement_unknown','balance_sheet_unknown']:
        if pk in idx: fin_pages.extend(idx[pk]['pages'])
        if len(fin_pages)>=4: break
    sample_pages=sorted(set(fin_pages))[:4] if fin_pages else [190,191,192,193]
    print(f'  Schema Mapper: using pages {sample_pages}')
    sample='\n'.join(tc.get(str(p),'')[:500] for p in sample_pages)
    prompt=(
        'You are mapping Excel template headers to financial PDF field names.\n'
        'Return ONLY valid JSON: {\"mappings\":[{\"excel_header\":\"\",\"pdf_field\":\"\",\"is_year\":false}],'
        '\"year_columns\":[],\"schema_mode\":\"standalone\"}\n\n'
        f'Excel headers: {json.dumps(headers)}\n\nPDF sample:\n{sample}'
    )
    result=safe_call(prompt)
    field_map={}
    for m in result.get('mappings',[]):
        eh=m.get('excel_header','').strip(); pf=m.get('pdf_field','').strip()
        if eh and pf:
            canonical_key=normalize(pf); field_map[normalize(eh)]=canonical_key
