TOC_PATTERN = r'(Balance Sheet|Statement of (?:Profit|Cash)|Profit (?:and|&) Loss|Cash Flow|Financial Highlights?)[\s\.]+([0-9]{1,3})'

def _auto_offset_enhanced(tc: dict) -> int | None:
    FOOTER_NUM    = re.compile(r'(\d+)\s*$')
    REPORT_FOOTER = re.compile(r'annual\s+report|integrated\s+report', re.IGNORECASE)
    LONE_INT      = re.compile(r'^\s*(\d{1,4})\s*$')

    votes = {}
    total = len(tc)
    step  = max(1, (total - 5) // 40)

    for i in range(5, total, step):
        text  = tc.get(str(i + 1), '')
        lines = [l.strip() for l in text.split('\n') if l.strip()]

        for line in lines[:3] + lines[-3:]:
            # Pattern 1: standalone number (original)
            m = LONE_INT.match(line)
            if m:
                n = int(m.group(1))
                if 5 < n < total * 3:
                    off = (i + 1) - n
                    votes[off] = votes.get(off, 0) + 1

            # Pattern 2: "Report YYYY  NNN" in footer (Infosys style)
            if REPORT_FOOTER.search(line):
                m = FOOTER_NUM.search(line)
                if m:
                    n = int(m.group(1))
                    if 5 < n < total * 3:
                        off = (i + 1) - n
                        votes[off] = votes.get(off, 0) + 2  # stronger signal

    if not votes:
        return None
    best = max(votes, key=votes.get)
    return best if votes[best] >= 3 else None

