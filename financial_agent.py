def do_consensus(ra,rb):
    ea={f"{e['metric']}__{e.get('year','?')}": e for e in ra.get('extractions',[]) if e.get('metric')}
    eb={f"{e['metric']}__{e.get('year','?')}": e for e in rb.get('extractions',[]) if e.get('metric')}
    out={}
    for key in set(ea)|set(eb):
        a,b=ea.get(key),eb.get(key)
        av=a.get('value') if a else None
        bv=b.get('value') if b else None
        if av is not None and bv is not None:
            if abs(av-bv)<=max(0.5,abs(av)*0.001):
                out[key]={**a,'confidence':0.98,'consensus':True,'flag':'OK',
                              'pages_verified': [a.get('page')] if a.get('page') else []}
            else:
                out[key]={'metric':a.get('metric'),'year':a.get('year'),
                          'value':None,'confidence':0.0,'agent_a':av,'agent_b':bv,'flag':'DISAGREE'}
        elif av is not None: out[key]={**a,'confidence':0.65,'flag':'SINGLE'}
        elif bv is not None: out[key]={**b,'confidence':0.65,'flag':'SINGLE'}
        else:
            base=a or b or {}
            out[key]={'metric':base.get('metric'),'year':base.get('year'),'value':None,'confidence':0.0,'flag':'NOT_FOUND'}
    return out

def _var(v):
    if v is None: return set()
    s,av,neg=set(),abs(int(float(v))),float(v)<0
    raw=str(av); r,tmp=raw[-3:],raw[:-3]
    while len(tmp)>2: r,tmp=tmp[-2:]+','+r,tmp[:-2]
    indian=(tmp+','+r if tmp else r)
    for fmt in [raw,f'{av:,}',indian]:
        s.add(f'({fmt})' if neg else fmt)
        if neg: s.add(f'-{fmt}')
    if isinstance(v,float) and v!=int(v): s.add(f'{v:.2f}')
    return s

def do_verify(f, tc, block_pages: list = None):
    """
    Hallucination detection — value must exist verbatim in source pages.

    Fix: checks ALL pages in the block (block_pages), not just the single
    cited page. This handles multi-page tables where the value may be on
    page 191 even though the LLM cited page 190 as the source.

    Records exact_page — the specific page where the value was found —
    for precise audit trail in the Excel Extraction_Log.
    """
    val = f.get('value')
    pg  = f.get('page')
    if val is None: return f

    variants = _var(val)
    try:
        variants.add(str(int(abs(float(val)))))
        variants.add(f'{float(val):.1f}')
        variants.add(f'{float(val):.2f}')
    except (ValueError, TypeError): pass
    variants = {v for v in variants if v}  # remove empty strings

    # Build search page list: cited page first, then all block pages
    pages_to_check = []
    if pg: pages_to_check.append(int(pg))
    if block_pages:
        for p in block_pages:
            if p not in pages_to_check:
                pages_to_check.append(p)

    if not pages_to_check:
        # No page info at all — can't verify, downgrade
        f.update({'confidence': 0.45, 'flag': 'UNVERIFIED', 'exact_page': None})
        return f

    # Search across all block pages — find EXACT page where value lives
    exact_page = None
    for search_pg in pages_to_check:
        page_text = tc.get(str(search_pg), '')
        if any(v in page_text for v in variants):
            exact_page = search_pg
            break

    if exact_page is None:
        # Value not found on any block page — hallucination signal
        f.update({'confidence': 0.45, 'flag': 'UNVERIFIED', 'exact_page': None})
    else:
        # Value verified — record the exact page it was found on
        f['exact_page'] = exact_page
        if exact_page != pg and pg is not None:
            # Found on a different page than LLM cited — correct the record
            f['page_corrected'] = True
            f['original_cited_page'] = pg
            f['page'] = exact_page  # update to correct page

    return f

