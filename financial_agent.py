def _rebuild_rev(canonical):
    rev = {}
    for k, vs in canonical.items():
        for v in vs:
            nk = re.sub(r'[^a-z0-9]','_',v.lower())
            nk = re.sub(r'_+','_',nk).strip('_')
            rev[nk] = k
    return rev

