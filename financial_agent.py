def _year_cols(ws,row):
    ymap={}
    for c in row:
        if c.column==1: continue
        for off in range(6):
            hv=ws.cell(max(1,c.row-off),c.column).value
            if hv:
                hv=str(hv)
                for pat,fmt in [
                    (r'FY\s*(\d{4})',lambda m:f'FY{m.group(1)}'),
                    (r'(\d{4})-(\d+)',lambda m:f'FY{m.group(1)}'),
                    (r'Year\s*(\d)',lambda m:f'Year{m.group(1)}'),
                    (r'\b(20\d{2})\b',lambda m:f'FY{m.group(1)}'),
                ]:
                    m2=re.search(pat,hv,re.IGNORECASE)
                    if m2: ymap[c.column]=fmt(m2); break
                if c.column in ymap: break
    return ymap

def _resolve(ws,cell):
    if cell.value is not None: return str(cell.value).strip()
    for mr in ws.merged_cells.ranges:
        if cell.coordinate in mr:
            top=ws.cell(mr.min_row,mr.min_col)
            if top.value: return str(top.value).strip()
    return ''

def _get_fact(facts, mk, yh):
    """
    Find a fact by metric key and year handle.
    Fix 6: also matches year='?' entries (when LLM didn't detect year)
    and partial year matches (2024 matches FY2024).
    """
    # 1. Exact match
    exact = facts.get(f'{mk}__{yh}')
    if exact and exact.get('value') is not None:
        return exact
    # 2. Partial year match: '2024' matches 'FY2024', 'FY2024' matches '2024'
    yh_digits = re.sub(r'[^0-9]', '', str(yh))
    for key, fact in facts.items():
        if not isinstance(fact, dict): continue
        if fact.get('metric') != mk: continue
        if fact.get('value') is None: continue
        fact_yr = str(fact.get('year', ''))
        fact_digits = re.sub(r'[^0-9]', '', fact_yr)
        if yh_digits and fact_digits and yh_digits in fact_digits:
            return fact
    # 3. year='?' fallback — LLM returned a value but missed the year
    unknown = facts.get(f'{mk}__?')
    if unknown and unknown.get('value') is not None:
        return unknown
    # 4. Order-based fallback (original logic)
    ORD = {'FY2024':0,'FY2023':1,'FY2022':2,'FY2025':0,'Year1':0,'Year2':1,'Year3':2}
    cands = sorted(
        [f for f in facts.values()
         if isinstance(f, dict) and f.get('metric') == mk and f.get('value') is not None],
        key=lambda f: ORD.get(str(f.get('year','')), 99)
    )
    return cands[0] if cands else None


def _find_label_cell(ws, row):
    """Scan all cells in row for the label — not just column A."""
    for cell in row:
        v = _resolve(ws, cell)
        if v and len(v.strip()) > 1:
            return cell, v.strip()
    return None, ''

def write_excel(excel_path,facts,out_path):
    wb=load_workbook(excel_path)
    G=PatternFill('solid',fgColor='E2EFDA')
    Y=PatternFill('solid',fgColor='FFEB9C')
    R=PatternFill('solid',fgColor='FFC7CE')
    log_rows=[]; stats=defaultdict(int)
    for sn in wb.sheetnames:
        ws=wb[sn]
        for row in ws.iter_rows():
            label_cell, lbl = _find_label_cell(ws, row)
            if not lbl: continue
            mk=normalize(lbl)
            if not mk: continue
            ycols=_year_cols(ws,row)
            for cell in row:
                if cell.column <= label_cell.column: continue  # skip label col and before
                if cell.value is not None: continue
                yh=ycols.get(cell.column,'FY2024')
                fact=_get_fact(facts,mk,yh)
                conf=fact.get('confidence',0) if fact else 0
                val=fact.get('value') if fact else None
                log_rows.append({'field':mk,'year':yh,'value':val,'conf':conf,
                                 'flag':fact.get('flag','') if fact else 'NOT_FOUND',
                                 'page':fact.get('page','?') if fact else '?',
                                 'src':fact.get('src','') if fact else ''})
                if val is None:
                    cell.value,cell.fill='N/A',R
                    cell.font=Font(color='9C0006',italic=True,size=9); stats['red']+=1
                elif conf>=0.85:
                    cell.value,cell.fill=val,G
                    cell.font=Font(color='0000CD')
                    cell.number_format='#,##0.00' if isinstance(val,float) else '#,##0'
                    cell.alignment=Alignment(horizontal='right')
                    try:
                        c=Comment(f"{mk}|{fact.get('year')}|p{fact.get('page','?')}|conf={conf:.2f}|{PROVIDER}",'Agent')
                        cell.comment=c
                    except: pass
                    stats['green']+=1
                else:  # yellow: medium conf OR UNVERIFIED (conf=0.45)
                    cell.value,cell.fill=val,Y
                    cell.font=Font(color='7F6000',italic=True if conf<0.60 else False)
                    cell.number_format='#,##0'; stats['yellow']+=1
    ls=wb.create_sheet('Extraction_Log')
    ls.append(['Field','Year','Value','Confidence','Flag','Source Page','Retrieval'])
    ls['A1'].font=Font(bold=True)
    for r in log_rows:
        ls.append([r['field'],r['year'],r['value'],f"{r['conf']:.2f}",r['flag'],r['page'],r['src']])
    wb.save(out_path)
    return dict(stats),log_rows

