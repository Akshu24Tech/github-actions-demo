def call_llm(prompt:str) -> dict:
    if PROVIDER=='gemini': return _gemini(prompt)
    if PROVIDER=='groq':   return _groq(prompt)
    if PROVIDER=='ollama': return _ollama(prompt)
    raise ValueError(f'Unknown: {PROVIDER}')

def _parse(t) -> dict:
    if not t: return {'extractions':[]}
    try: return json.loads(t)
    except:
        m=re.search(r'\{.*\}',str(t),re.DOTALL)
        if m:
            try: return json.loads(m.group())
            except:
                pass
    return {'extractions':[]}

def _gemini(p):
    from google import genai; from google.genai import types
    c=genai.Client(api_key=GEMINI_KEY)
    r=c.models.generate_content(model='gemini-2.5-flash',contents=p,
      config=types.GenerateContentConfig(temperature=0.0,max_output_tokens=4096,response_mime_type='application/json'))
    return _parse(r.text)

def _groq(p):
    import groq; c=groq.Groq(api_key=GROQ_KEY)
    r=c.chat.completions.create(model='llama-3.3-70b-versatile',
      messages=[{'role':'system','content':'Financial extraction AI. Valid JSON only.'},{'role':'user','content':p}],
      temperature=0.0,max_tokens=4096,response_format={'type':'json_object'})
    return _parse(r.choices[0].message.content)

def _ollama(p):
    import urllib.request
    payload=json.dumps({'model':'llama3.2','prompt':p,'stream':False,'format':'json','options':{'temperature':0.0}}).encode()
    req=urllib.request.Request('http://localhost:11434/api/generate',data=payload,headers={'Content-Type':'application/json'})
    with urllib.request.urlopen(req,timeout=120) as r:
        return _parse(json.loads(r.read()).get('response',''))

def safe_call(p,retries=3):
    for i in range(retries):
        try: return call_llm(p)
        except Exception as e:
            err=str(e).lower()
            if any(x in err for x in ['rate','limit','overload','429','503']):
                w=5*(2**i); print(f'  Rate limit. Wait {w}s...'); time.sleep(w)
            else:
                print(f'  LLM error: {e}'); time.sleep(2)
    return {'extractions':[]}

